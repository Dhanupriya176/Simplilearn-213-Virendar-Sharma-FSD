package practice.programs;

public class Info {
	private  String ID ;
	private String name;  
	private String age;
	private String dept;  
	

	public Info(){}


	public String getID() {
		return ID;
	}

	public void setID(String ID) {
		this.ID = ID;
	}  
	public String getName() {
		return name;
	}

	public void setname(String name) {
		this.name = name;
	}

	public String getage() {
		return age;
	}

	public void setage(String age) {
		this.age = age;
	}
	public String getdept() {
		return dept;
	}

	public void setdept(String dept) {
		this.dept = dept;
	}  
	
	
}
